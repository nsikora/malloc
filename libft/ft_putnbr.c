#include "libft.h"

void	ft_putnbr(size_t n)
{
	size_t i;

	i = n;
	if (n < 0)
	{
		ft_putchar('-');
		i = i * -1;
	}
	if (i > 9)
		ft_putnbr(i / 10);
	ft_putchar(i % 10 + 48);
}
